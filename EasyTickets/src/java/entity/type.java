public class type {
     private int id;
    private String typename;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getTypename() {
        return typename;
    }

    public void setTypename(String typename) {
        this.typename = typename;
    }

    public type(int id, String typename) {
        this.id = id;
        this.typename = typename;
    }
    
    
}
